// Mapeo de categoría a icono
export const getCategoryIcon = (category) => {
  const icons = {
    'Frutas y Verduras': IconCategoryFruits,
    'Lácteos': IconCategoryDairy,
    'Panadería': IconCategoryBakery,
    'Carnes': IconCategoryMeat,
    'Pescados y Mariscos': IconCategoryFish,
    'Fiambres y Embutidos': IconCategoryHam,
    'Bebidas': IconCategoryDrinks,
    'Congelados': IconCategoryFrozen,
    'Enlatados': IconCategoryCanned,
    'Arroz, Pasta y Cereales': IconCategoryRice,
    'Aceites y Aderezos': IconCategoryOil,
    'Condimentos y Especias': IconCategorySpices,
    'Café, Té y Chocolate': IconCategoryCoffee,
    'Galletitas y Dulces': IconCategoryCookies,
    'Limpieza': IconCategoryCleaning,
    'Higiene Personal': IconCategoryHygiene,
    'Perfumería': IconCategoryPerfume,
    'Otros': IconCategoryOther
  }
  return icons[category] || IconCategoryOther
}

